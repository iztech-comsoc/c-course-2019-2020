void test_my_strlwr() {
    IzTest* t = iztest_new("my_strlwr");

    char r1[] = "THIS is A teST STriNG.";
    my_strlwr(r1);
    iztest_eq(t, "my_strlwr(\"THIS is A teST STriNG.\")", strcmp(r1, "this is a test string."), 0);

    char r2[] = "123456";
    my_strlwr(r2);
    iztest_eq(t, "my_strlwr(\"123456\")", strcmp(r2, "123456"), 0);

    char r3[] = "";
    my_strlwr(r3);
    iztest_eq(t, "my_strlwr(\"\")", strcmp(r3, ""), 0);

    char r4[] = "d";
    my_strlwr(r4);
    iztest_eq(t, "my_strlwr(\"d\")", strcmp(r4, "d"), 0);

    char r5[] = "aaaaa;sddsdsds";
    my_strlwr(r5);
    iztest_eq(t, "my_strlwr(\"aaaaa;sddsdsds\")", strcmp(r5, "aaaaa;sddsdsds"), 0);

    iztest_print_results(t);
}

