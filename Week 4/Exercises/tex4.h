void test_index_of_char() {
    IzTest* t = iztest_new("index_of_char");

    iztest_eq(t, "index_of_char(\"n\")", index_of_char('n'), 0);
    iztest_eq(t, "index_of_char(\"o\")", index_of_char('o'), 1);
    iztest_eq(t, "index_of_char(\"r\")", index_of_char('r'), 12);
    iztest_eq(t, "index_of_char(\"\\n\")", index_of_char('\n'), 38);
    iztest_eq(t, "index_of_char(\".\")", index_of_char('.'), -1);

    iztest_print_results(t);
}